def calculate(first):
    operation = input("Pick an operation ( +, -, *, /): ")
    second = float(input("Enter the second number: "))

    if operation == "+":
        result = first+second
    elif operation == "-":
        result = first-second
    elif operation == "*":
        result = first*second
    elif operation == "/":
        result = first/second
    else:
        return "Invalid operator"

    print(f"{first} {operation} {second} = {result}")
    bFlag = input(f"Type 'y' to continue calculating with {result}, or type 'n' to start a new calculation: ")
    if bFlag=="y":
        calculate(result)
    else:
        return

first = float(input("Enter the first number: "))
calculate(first)